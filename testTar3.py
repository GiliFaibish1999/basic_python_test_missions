n=int(input('Please enter a positive number: '))
for x in range(1, n+1):
    for y in range(1, n+1):
        z = x * y
        if (z%3 == 0):
            print(z, end="\t")
        else:
            print("*", end="\t")
    print()
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
